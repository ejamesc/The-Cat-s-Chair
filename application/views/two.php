<h1>2.</h1>

<p id="bigger">The cat had not left.</p> 

<p>It sat on <?=$bro_name?>&#x27;s chair, curled up, studying us, occasionally flicking its tail. It was a big cat. A tom. It had one ravaged ear, and there were patches of fur that looked like it had been torn out sometime in the past. </p>

<p>&quot;Who?&quot; I asked.</p>

<p>My mother pointed at my father, who hurriedly hid behind a newspaper. &quot;Alright,&quot; I said, &quot;Who bought it?&quot;</p>

<p>Both of them shook their heads. My mother wrung her hands in exasperation. &quot;It followed us back from the flea market. I don&#x27;t know how - but when I opened the door I saw was this orange flash of fur and then -&quot;</p>

<p>The cat raised a paw and started washing its face. The four of us looked at it again.</p>

<p>&quot;It&#x27;s in my chair!&quot; <?=$bro_name?> wailed, &quot;Get rid of it!&quot; This was unfortunate. My parents had, just over a month ago, bought the armchair from one of the many flea markets they had visited. My brother was happy to finally have a spot to call his own.</p>

<p>&quot;Do it yourself.&quot; I retorted, and I made to turn away.</p>

<p>But the pleading look on my mother&#x27;s face and my father&#x27;s bandaged hand made me stop. <?=$bro_name?> took a step towards the cat. It looked up at him and hissed. <?=$bro_name?> took a step back.</p>

<p>&quot;What fish do you have in the fridge?&quot; I asked mum.</p>

<p>&quot;Salmon.&quot;</p>

<p>&quot;Any leftovers?&quot;</p>

<p>We brought it out and waved it in front of her. The cat closed her eyes and waved its tail, as if smelling a banquet of flowers.</p>

<p>&quot;Here, kitty,&quot; I said, &quot;Kitty kitty kitty ...&quot;</p>

<p>&ldquo;<?=$my_name?> &mdash;&rdquo; my mum warned.</p>

<p>The cat reopened her eyes and turned their full power on me. The effect was unsettling: for a few minutes I found myself swimming in the inky blackness of her dilated pupils.</p>

<p>I retreated.</p>

<p>Cat: 1; Family: 0</p>

<p>In the end, we decided to leave it alone, hoping it would tire and leave. No such luck. My father turned to CNN, hoping to bore it to death, but when I peeked into the family room it was my father who was asleep and not the cat. To tell you the truth the cat seemed very interested in Israel&#x27;s war with Lebanon.</p>

<p>&quot;Lights out.&quot; My mother said. &quot;<?=$my_name?>, go wake up your father - We&#x27;ll leave the cat overnight.&quot;</p>

<p>&quot;What if it poops in my chair?&quot; <?=$bro_name?> wailed.</p>

