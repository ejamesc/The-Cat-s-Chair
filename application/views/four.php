<h1>4.</h1>

<p id="bigger">The next day brought good news. </p>

<p>It turned out the evening before father had slipped out and placed an ad about our problem in the Morning Post. A woman had called in the morning and told him that she was coming around 4 to see if the cat was hers.</p>

<p>&quot;Orange markings, wild look, very ferocious?&quot; she had said over the telephone, &quot;Sounds like my <?=$cat_name?>!&quot;</p>

<p>Our hopes were up. I told <?=$friend_name?> about it during English and we both waited impatiently for school to end. But there were a few things that occupied my mind. For example: how was she going to get the cat out of <?php echo($bro_name);?>&#x27;s chair?</p>

<p>We were soon going to find out. <?=$friend_name?> and I spent the afternoon trying to make a dent in the History homework our teacher had set us, while munching crackers, keeping an eye on the clock and stealing glances at the now very hungry cat. He repeatedly made a sound that sounded like a haughty &quot;mrauf!&quot; every time we took a cracker out of the tin.</p>

<p>At exactly four an old van drove up to our house and the doorbell rang. My mother hurried to the door and opened it to reveal an old woman with greying hair and even, white teeth. She was smiling at us.</p>

<p>&quot;Hello,&quot; she said, &quot;Where&#x27;s <?=$cat_name?>?&quot;</p>

<p>We led her to the family room, where she gave a cry of delight and launched herself at the cat. We blinked; the cat was on her lap and the old lady was in the chair.</p>

<p>&quot;Do you know this chair belonged to me, once?&quot; the woman said, stroking the armrest with one hand and the cat with the other. &quot;It was <?=$cat_name?>&#x27;s favourite. The old boy must&#x27;ve hunted it down after it got stolen a month or two ago.&quot;</p>

<p>&quot;You haven&#x27;t seen her for that long?&quot; My mother asked, awed. &quot;I got the chair at a flea market - I never guessed that it was - &quot;</p>

<p>&quot;Not to worry, not to worry,&quot; reassured the old lady. She got to her feet and put <?=$cat_name?> back in the chair. &quot;I&#x27;ll pay for it and reimburse you for your trouble. Then let&#x27;s lug it into my van - oh you poor boy, I&#x27;ve never seen you so thin ...&quot;</p>

<p>The conclusion to the crisis seemed near, so <?=$friend_name?> left for the toilet at the back and I went to find my father. Both my parents carried it to the back of the van and we stood waving goodbye as the old lady and <?=$cat_name?> drove off.</p>

<p>&quot;Well.&quot; said my mother as we went into the house again, &quot;All&#x27;s well that ends well, I suppose.&quot;</p>

<p>&quot;And to think that the cat traveled all that way just for one chair -&quot; The day was beautiful &mdash; white streaks in a sapphire sky and flowers the smell of wine. I hadn&#x27;t before realized how much we&#x27;d suffered in the hands of one cat.</p>

<p>At that precise moment <?=$friend_name?> came in from the toilet. She was pale and shaking, and we all paused to look at her. The smile froze on my face.</p>

<p>She struggled to get the words out: &quot;Do you - by any chance - keep a python in the aquarium at the back?&quot;</p>
